/**
 * op_add - add two numbers
 * @a: number to add
 * @b: number to add
 * Return: result of operate
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction two numbers
 * @a: number to operate
 * @b: number to operate
 * Return: result of operate
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: number to operate
 * @b: number to operate
 * Return: result of operate
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers
 * @a: number to operate
 * @b: number to operate
 * Return: result of operate
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - get mod two numbers
 * @a: number to operate
 * @b: number to operate
 * Return: result of operate
 */
int op_mod(int a, int b)
{
	return (a % b);
}
